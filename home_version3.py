import turtle
import math
t = turtle.Turtle()
t.pensize(3)
turtle.bgcolor("skyblue")
def move(x,y):
    t.penup()
    t.goto(x,y)
    t.pendown()
def hcn(d,r):
    for i in range(2):
        t.fd(d)
        t.lt(90)
        t.fd(r)
        t.lt(90)
def tamgiaccan(h,goc):
    t.fd(h)
    t.lt(goc)
    t.fd(h*(math.sqrt(2)/2))
    t.lt(90)
    t.fd(h*(math.sqrt(2)/2))
    t.lt(goc)
def hinhthang(d,h,goc):
    t.fd(d)
    t.lt(goc)
    c = h/math.sin(math.radians(180-goc))
    t.fd(c)
    t.lt(180-goc)
    t.fd(d-(math.cos(math.radians(180-goc))*c*2))
    t.lt(180-goc)
    t.fd(c)
    t.lt(100)
def ongkhoi():
    move(-150, 100)
    t.fillcolor("orange")
    t.begin_fill()
    t.lt(135)
    t.fd(20)
    t.rt(45)
    t.fd(30)
    t.lt(90)
    t.fd(15)
    t.lt(90)
    t.fd(13)
    t.end_fill()
    move(-165, 180)
    t.fd(15)
    move(-180, 200)
    t.fd(30)
    t.lt(90)

def left(x,y):
    t.lt(x)
    t.fd(y)

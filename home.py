import turtle
t = turtle.Turtle()
def square(x, y):
    for i in range(4):
        t.fd(x)
        t.lt(y)
def move(x, y):
    t.penup()
    t.goto(x, y)
    t.pendown()
def door(x,y,z):
    for i in range(2):
        t.fd(x)
        t.lt(y)
        t.fd(z)
        t.lt(y)
def triangle():
        t.lt(45)
        t.fd(141.421356)
        t.rt(90)
        t.fd(141.421356)
def right(x,y):
    t.rt(x)
    t.fd(y)
def left(x,y):
    t.lt(x)
    t.fd(y)
def tree():
    left(45, 106.0660172)
    right(90, 106.0660172)
    right(135, 150)
turtle.bgcolor("pink")
t.pensize(3)
move(-300,-100)
t.fillcolor("violet")
t.begin_fill()
square(200,90)
t.end_fill()
move(-225,-100)
t.fillcolor("blue")
t.begin_fill()
door(50,90,100)
t.end_fill()
move(-300,100)
t.fillcolor("orange")
t.begin_fill()
triangle()
t.end_fill()
